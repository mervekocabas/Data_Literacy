# Function to extract universities that are in the JSON file but not in the university_country_map
def extract_universities_not_in_map(json_file_path):
    """
    Extract university names from the JSON file that are not found in the university_country_map.
    
    Args:
        json_file_path (str): Path to the input JSON file.
        university_country_map (dict): A dictionary with university names as keys and their respective countries as values.
    
    Returns:
        list: A list of university names not found in the university_country_map.
    """
    try:
        with open(json_file_path, 'r') as file:
            data = json.load(file)
        
        # List to hold universities not found in the map
        universities_not_in_map = []
        
        # Iterate through the JSON data and check the affiliations
        for entry in data:
            if "aff" in entry and entry["aff"]:
                affiliations = [aff.strip() for aff in entry["aff"].split(";") if aff.strip()]
                for aff in affiliations:
                    if is_university(aff):
                        # Check if the affiliation is a university, and not already in the map
                        for university_name in university_country_map.keys():
                            if university_name.lower() in aff.lower():  # Case insensitive match
                                break
                        else:
                            # If no match was found in the map, add the university name
                            universities_not_in_map.append(aff)

        # Remove duplicates by converting the list to a set and back to a list
        return list(set(universities_not_in_map))

    except FileNotFoundError:
        print(f"File not found: {json_file_path}")
        return []
    except json.JSONDecodeError:
        print(f"Invalid JSON in file: {json_file_path}")
        return []
    except Exception as e:
        print(f"An error occurred: {e}")
        return []

